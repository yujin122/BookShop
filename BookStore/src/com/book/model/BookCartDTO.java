package com.book.model;

public class BookCartDTO {
	private int c_num;
	private int c_sale_num;
	private String c_member;
	private int c_qty;
	private String c_date;
	
	public int getC_num() {
		return c_num;
	}
	public void setC_num(int c_num) {
		this.c_num = c_num;
	}
	public int getC_sale_num() {
		return c_sale_num;
	}
	public void setC_sale_num(int c_sale_num) {
		this.c_sale_num = c_sale_num;
	}
	public String getC_member() {
		return c_member;
	}
	public void setC_member(String c_member) {
		this.c_member = c_member;
	}
	public int getC_qty() {
		return c_qty;
	}
	public void setC_qty(int c_qty) {
		this.c_qty = c_qty;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String c_date) {
		this.c_date = c_date;
	}
	
	
}
